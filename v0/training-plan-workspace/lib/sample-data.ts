export enum ExerciseType {
  WarmUp = 1,
  Gym = 2,
  Circuit = 3,
  Isometric = 4,
  Plyometric = 5,
  Sprint = 6,
  Drill = 7,
}

export interface Exercise {
  id: number
  name: string
  order: number
  exercise_type: ExerciseType
  // Basic parameters
  sets: number
  reps: string
  weight?: string
  rest_time?: number // in seconds
  rpe?: number
  // Advanced parameters
  tempo?: string // e.g., "3-0-1-0"
  distance?: number // in meters
  duration?: number // in seconds
  power?: number // in watts
  velocity?: number // in m/s
  effort?: string // e.g., "moderate", "hard"
  height?: number // in cm
  resistance?: string // e.g., "light band", "heavy band"
  performing_time?: number // in seconds
  notes?: string
  superset_group?: string // for grouping exercises in supersets
}

export interface Session {
  id: number
  name: string
  type: "speed" | "strength" | "endurance" | "recovery" | "technique"
  duration: number
  exercises: Exercise[]
  volume: number
  intensity: number
  notes?: string
}

export interface Microcycle {
  id: number
  name: string
  weekNumber: number
  startDate: string
  endDate: string
  sessions: Session[]
  volume: number
  intensity: number
  isDeload: boolean
  focus: string
}

export interface Mesocycle {
  id: number
  name: string
  phase: "GPP" | "SPP" | "Taper" | "Competition"
  startDate: string
  endDate: string
  microcycles: Microcycle[]
  color: string
  description: string
  totalSessions: number
  avgVolume: number
  avgIntensity: number
}

export interface RaceAnchor {
  id: number
  name: string
  date: string
  type: "primary" | "secondary"
  event: string
}

export interface TrainingPlan {
  id: number
  name: string
  startDate: string
  endDate: string
  mesocycles: Mesocycle[]
  raceAnchors: RaceAnchor[]
  status: "active" | "draft" | "completed"
}

