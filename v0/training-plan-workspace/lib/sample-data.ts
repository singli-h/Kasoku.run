// Generate sample sessions with 10-12 exercises each
function createSession(
  id: number,
  name: string,
  type: Session["type"],
  exerciseList: Array<Partial<Exercise>>,
  volume: number,
  intensity: number,
): Session {
  const exercises = exerciseList.map(
    (ex, index) =>
      ({
        ...ex,
        id: ex.id || id * 100 + index,
        order: index + 1,
        name: ex.name || "Exercise",
        exercise_type: ex.exercise_type || ExerciseType.Gym,
        sets: ex.sets || 3,
        reps: ex.reps || "10",
      }) as Exercise,
  )

  return {
    id,
    name,
    type,
    duration: 90,
    exercises,
    volume,
    intensity,
  }
}

